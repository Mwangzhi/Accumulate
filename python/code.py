#   定义默认参数要牢记一点：默认参数必须指向不变对象！


def add(L=[]):
    L.append('END')
    return L


print(add())  # ['END']
print(add())  # ['END', 'END']


def add(L=None):
    if L is None:
        L = []
    L.append('END')
    return L


print(add())  # ['END']
print(add())  # ['END']
